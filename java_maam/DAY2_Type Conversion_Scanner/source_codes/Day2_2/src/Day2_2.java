
// Variables Demo 
//while declaring the variables we specify data type COMPULSORY
// java is called as Statically Type Checked Language


// javac error because variable is not assigned with value
public class Day2_2 
{

	public static void main(String[] args) 
	{
		int num; // variable declaration 
		// num is uninitialized variable
		// java does not allow uninitialized variable 
		// javac error 
		System.out.println("Value of Num = "+num);
	}
}



