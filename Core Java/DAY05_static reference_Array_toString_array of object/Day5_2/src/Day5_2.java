//static import


import static java.lang.Math.*;
import static java.lang.System.*;

public class Day5_2 {

	public static void main(String[] args) 
	{
		out.println("PI = "+PI);
		out.println("Min = "+min(30,25));
		out.println("Min = "+min(5.5,3.5));
		out.println("Max = "+max(88,99));
		out.println("sqrt = "+sqrt(25));
		out.println("Power = "+pow(6, 3));
		
		

	}

}




